const sensorNameList = sensorList.map((sensor) => sensor.name);

export const querySensorConnection = ({
  after,
  before,
  filterText = "",
  first,
  last,
}: {
  after?: string | undefined;
  before?: string | undefined;
  filterText?: string | undefined;
  first?: number | undefined;
  last?: number | undefined;
}) => {
  const connectionOptions = { after, before, first, last };

  if (filterText === "") {
    const searchNodeList = sensorList.map((sensor) => {
      const node = sensor;
      const info = { highlightedText: null };
      return { info, node };
    });
    return connectionFromSearchArray(searchNodeList, connectionOptions);
  }

  const searchResult = uFuzzy.search(sensorNameList, filterText);
  const searchTextList = searchResult[0] ?? [];
  const searchInfo = searchResult[1];

  const searchNodeList = searchTextList.map((sensorIndex, searchIndex) => {
    const node = sensorList[sensorIndex] ?? null;
    const info = { highlightedText: node == null ? null : highlight(node.name, searchInfo!.ranges[searchIndex] ?? []) };  // eslint-disable-line @typescript-eslint/no-non-null-assertion
    return { info, node };
  });

  return connectionFromSearchArray(searchNodeList, connectionOptions);
};
